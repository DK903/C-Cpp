#include <stdio.h>
int main(){
	int a = 0;//int类型是4个字节  也就是32bit 
	//00000000000000000000000000000000
	int b = ~a;
	//~按二进制位取反
	//11111111111111111111111111111111 
	//最高位是1，表示是负数，而在内存中存储的是补码
	//因此 反码为：补码-1 
	//11111111111111111111111111111110
	//原码为： 最高位不变  其余位取反 
	//10000000000000000000000000000001  即-1 
	printf("%d\n",b);
	
	return 0;
}
