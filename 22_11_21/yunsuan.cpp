#include <stdio.h>
int main(){
	short int a = 16 ;
	short int x = -1; // 存在内存中是补码的形式 
	// 	   1000000000000001 -- 原码 
	//	   1111111111111110 -- 反码
	//     1111111111111111 -- 补码 
	int b = a >> 2;// 除2 
	//     |0000000000010000|
	//     |0000000000001000|0
	// 算术右移：右边丢弃，左边补原符号位 
	int c = a << 1;
	int y = x >> 1;// 乘2  
	//     |0000000000010000|
	//    0|0000000001000000|-- 补 0  
	// 左边丢弃，右边补 0 
	printf("%d\n",b);
	printf("%d\n",y);
	printf("%d\n",c);
	return 0;
}
