#include <stdio.h>
//整型提升 
int main(){
	char a = 3;
	// char类型 一个字节 补码
	// a - 00000011
	//	00000000000000000000000000000011
	char b = 127;
	// b - 01111111
	//  00000000000000000000000001111111
	char c = a + b;
	// a + b
	// 00000011 + 01111111
	// c - 10000010 整型提升：因为 %d 
	// 按最高位补 
	//=11111111111111111111111110000010   -- 补码
	// 11111111111111111111111110000001   -- 反码
	// 10000000000000000000000011111110   -- 原码 
	// 即 -126  
	printf("%d\n",c); 
	return 0; 
} 
